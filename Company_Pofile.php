                            <h4>6. ANALYTICAL WORK</h4>
                            <p>GSRF  conducts  analytical  assignments  regularly  on  behalf  of  leading edible  oil  and  food  industries  of  India.  Analytical  work      includes  full analysis of edible oil and food samples (according to PFA/BIS/Agmark specifications); analysis of fatty acid composition, vitamin components, micronutrients,   presence   of   contaminants   with   a   view   to   ensure conformance of quality products to specific standards.</p>
                            <p>The    Industries  which  sent  their  samples  for  analysis  of  oil,  food  and animal  feed  included  Dhara  Vegetable  Oil  and  Foods  Company  Ltd., HAFED Oil Mills, NAFED, Adani Wilmar Ltd, Recon Oil Industries, New Delhi,  Chemical  Construction  International  Pvt  Ltd,  New  Delhi,  Raj Agro Mills, New Delhi, Sethia Oil Mills Burdwan and Sahapur units,JVL Agro  Foods,  Alwar  and  others,  Rites  Ltd,Kwality  Dairy  Ltd,Particular mention   may  be   made  of  Detailed    analysis  of  packaged  foods according  to  labeling  requirement  (Prevention  of  Food  Adulteration Rules, 1955).,Analysis of double low variety of canola (low erucic acid, low   glucosinolate)   given   by   TERI,   detailed   analysis   of   biodiesel samples  and  raw  materials  (fish  oil,  fatty  acid  distillates  etc)  according to ASTM specifications.</p>
                            <p>GSRF   acts   also   as   information   and   advice   bureau   by   offering consultancy service in the form of status /feasibility reports, informationpapers,   problem   diagnosis,   devising   cost-effective   solutions   and updating the latest R&D findings</p>
