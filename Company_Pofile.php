                            <centeR> <h3> THE REPORT </h3> </centeR>
                            <br/>
                            <h4> 1. PAST ACHIEVEMENTS </h4>
                            <p> •	This report, apart from giving the statutory information for the financial year 2013-14, also summarizes the achievements for the past since the inception of the Foundation.  The Ganesh Scientific Research Foundation has since its establishment in 1978, been actively involved in undertaking research projects on behalf of the various government research and development institutions, private and public bodies. The Foundation has also served the nation through associating actively in standardization of various techniques for production and processing as well as in standardizing the methods of testing the purities and adulterants in many edible products. The Foundation has also been associated with private and public organizations in product development and diversification. Many government and private institutions have benefited from the research and developmental back up provided. It should be noted that most of the activities have been carried out by the Foundation at cost with no financial benefits accruing to it directly or indirectly.
                            <br/>
                            •	The Foundation had been actually a research and development arm of the Hindustan Vegetable Oils Corporation Ltd ,  and the Directorate of Vanaspati, Oils and Fats, Ministry of Consumer Affairs, Food and Public Distribution, Government  of India. The Foundation continues to undertake analytical assignments, preparation of status report on specific topics and research projects on behalf of Directorate of Vanaspati and other departments of Central and State Governments.
                            </p>
                            
                            <h4> 2. RESEARCH AND DEVELOPMENT </h4>
                            <p> As mentioned, Ganesh Scientific Research Foundation since its establishment in 1978, has been actively involved in undertaking research projects assigned by various Government Departments and industrial organizations, private and public bodies in the fields of oil and oil-based products, animal feed, food and related materials, biofuels, oil substitutes etc. Research was  conducted on diverse aspects of  food safety and nutrition, micronutrient enrichment of oils, oil based products and common food items, detection of adulteration and contaminants, utilization of indigenous nontraditional resources to increase the availability of food, oil and animal feed, detoxification of nontraditional oilcakes for utilization as animal feed, micronutrient enrichment and recovery of value-added by-products such as wax, lecithin, vitamin E, oryzanol and fatty acids, from the vegetable oil refining industry, development of interesterified fat based Vanaspati, margarine etc. 
                                <br/>The Foundation has conducted successfully many important projects on behalf of the Directorate of Vanaspati, Oils and Fats, Ministry of Consumer Affairs, Food & Public Distribution, Government of India, Council of Scientific and Industrial Research, Ministry of Food etc.  
                                <br/>Some project titles  are  Blending of Oils; Development of Soya Dal; Recovery of Oil from Spent Bleaching Earth; Preparation of Industrial Lipases;  Study on Shelf Life of RBD Palmolein; Study on the use of Mustard Oil in Vanaspati;  Upscaling Inter-esterification  Process to Pilot Plant Stage; Preparation of Low Trans-nutritious Margarine and Shortening Fat Bases; Development of Micro-Nutrient enriched High Performance Multiple Blended Oil Mediums; Development of High Performance Oil Blends; Development Of Medium Chain.
                                <br/>Triglycerides; Recovery, Upgrading, Modification of Lecithin from Rice Bran Oil; Detection of Argemone Oil in Edible Oils; Scale Up Trial of Partial Hydrogenation of Soybean Oil; Recovery of Useful Byeproducts Like Lecithin from Indigenous Non-traditional Oil Sources; Development of Salolein and Mahuaolein as Direct Cooking Oil Analogous to Palmolein etc.
                                <br/> Department of Bio-technology, Government of India  identified GSRF as a participant in a multi-institutional project to conduct research on “Detoxification and Utilization of Key Agro-Forest Based Non 
                                        Conventional Oil Cakes in the Feeding of Livestock’. In addition, the Foundation had also been associated in another DBT financed research project on biotech feed. 
                                <br/>The Foundation has served the nation also through active association in standardization of various techniques and parameters for production and processing as well as in standardizing methods of testing the purity and adulterants in edible products. Along-with, the Foundation has been associated with private and public organizations in new product development and diversification. 
                                <br/>The Foundation has, on behalf of the Bureau of Indian Standards and the Central Committee on Food Standards, established and standardized methods for colour developments in Baudoin test used for detection of adulteration of sesame oil and vanaspati; developed analytical methods for detecting adulteration of oils in oil-admixture; detection of linseed oil in soyabean oil; detection of taramira, argemone, linseed oil, karanja oil, ambadi seed oil, in other oils.  GSRF has helped in preparation of Draft Standards for: sensory evaluation of edible fats and oils; criteria of edibility of fats and oils; detection of rice bran oil in other oils; and method of Baudoin test.
                            </p>
                            
