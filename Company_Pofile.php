                            <h4> 16. RESEARCH ACTIVITIES </h4>
                            <br/>
                            <ol> 
                                <li> <p>Ganesh Scientific Research Foundation has been conducting research in projects assigned by various Government Departments and industrial organizations in the field of oil and oil-based products, animal feed, food and related materials. Research  has been conducted on  food safety and nutrition, micronutrient enrichment of oils, oil based products and common food items, detection of adulteration and contaminants, utilization of indigenous nontraditional resources to increase the availability of food, oil and animal feed, detoxification of nontraditional oilcakes for utilization as animal feed, micronutrient enrichment and recovery of valueadded by-products such as wax, lecithin, vitamin  E, oryzanol and fatty acids, from the vegetable oil refining industry, development of interesterified fat based Vanaspati, margarine etc. All research investigations were conducted according to the work plans approved by concerned authorities. Some of the research activities undertaken in the present year are discussed. </li>
                                <li> 
                                    <b> “Detoxification and Utilization of Agro-forest based unconventional oilcakes in the feeding of livestock” financed and supported by the Department of Biotechnology, Government  of India. </b>
                                    <ul> 
                                        <li> <b> Genesis of the project: <b/> <p> National agriculture policy has stressed strongly upon the development of animal husbandry and dairying, assigning high priority in the efforts for diversifying agriculture, increasing protein availability in food basket and for generating exportable surpluses, thereby generating wealth and employment in the agriculture sector. To achieve this objective and to optimize the output of livestock products, one significant strategy comprises of expanding current animal feed base and improving nutrient extraction from the available biomass . Proteins are one of the major costly items in the livestock diets and usually deficient to the tune of 50% of the requirements in the roughage based feeding systems. Poor dietary supply of proteins results in low rates of production and reproduction as well as increased susceptibility of livestock to diseases. For improving production, efficiency of utilization of the available conventional and non-conventional protein rich feed resources needs to be maximized. </p> </li>
                                        <li><p> Despite being rich in nutritive value, some of the known and emerging unconventional feeds like neem seed kernel cake, karanj cake and jatropha meal are hitherto being wasted due to presence of one or the other toxic factors and are not being used effectively as a component of animal feed. Available processing (chemical, physical, biological) techniques of these non-conventional feeds may reduce or ameliorate harmful factors partially or completely to render them as a valuable wholesome protein supplements; however, these techniques need to be refined, reevaluated and developed for market viability. In this context, Dept of Biotechnology, Government  of India convened a Brain Storming Meeting on 25th May 2006   inviting experts in the field of animal nutrition to identify important areas of research and chalk out future research strategies for improving bio-availability of proteins from potential protein sources. The present project proposal on <b> Detoxification and utilization of key agro-forest based nonconventional oilcakes in the feeding of livestock </b> was submitted to Department of Biotechnology in tune with the recommendations of Brain Storming Meeting held at DBT. It was decided that a multicentric project on detoxification of oil cakes will be formulated by Dr. K. Sharma, IVRI along with Ayurvet as an Industrial Partner and Ganesh Scientific Research Foundation was identified as one of the participants of above multi-institutional project.  </li>
                                        <li> <b> Objectives of the project: </b><p> The project was aimed to facilitate large scale availability and utilization of non-conventional protein rich agro-forest based oil cakes in the diet of livestock for enhancing their productivity with following specific objectives: <br/>  &nbsp;&nbsp;&nbsp;•	To assess the respective efficiency of detoxification methods available for karanja, jatropha and neem cakes and their feasibility for commercialization.  <br/> &nbsp;&nbsp;&nbsp;•	To evolve economic rations by effective use of detoxified oil cakes for meat and milk production. </p></li>
                                    </ul>        
                                
                                </li>
                                <li> <b>  Progress of the Project </b> 
                                    <ul> 
                                        <li> <b>Comprehensive survey report- </b> <p>In the first phase, a comprehensive survey report was prepared by GSRF on various aspects on the three selected cakes viz., chemical composition, nature and level of toxic principles, available detoxification methods and respective efficiencies etc. </p> </li>
                                        <li> <p>Collection of Neem, Karanja, And Jatropha Seed Samples From Different Agroclimatic Zones Of India. A list of Neem, Karanja, and Jatropha seed manufacturers/suppliers was prepared based on information collected from internet and other sources. Around 100 manufacturers were contacted for the purpose and samples were collected. So far, we have ten Jatropha, eleven Neem and three Karanja samples. Around 2 kgs of each sample were sent to Ayurvet, for authentication, alongwith botanical descriptors. Required amounts of jatropha,neem and karanja seeds were cleaned, cracked manually without damaging the seeds and the kernels stored in plastic containers at room temperature prior to further analysis. </p> </li>
                                        <li> <p>Physical Properties of Seeds (Jatopha, Neem, Karanja): For measurement of Physical properties of seeds, twenty seeds were randomly taken from each sample. The average weights, length, width of the seeds were determined. The seeds were cracked manually without damaging the seeds, the shells were carefully removed and average weight of the kernel were determined .The average shell weight was obtained by subtracting the wt of kernel from the wt of the respective seeds.  </p></li>
                                        <li> <b>Oil Extraction for Preparation of Oilcake Samples: </b> <p> Required amounts of   seeds were dehulled, kernels were ground using a mechanical grinder, and defatted in a Soxhlet apparatus using petroleum ether (b.pt  40-60 deg c) for 16 hrs. The defatted kernel meal was air dried at room temperature and stored in plastic containers.
                                                <br/> &nbsp;&nbsp;&nbsp;•	Analysis of chemical, biochemical components and anti-nutritional factors in various cakes were carried out in accordance with the standard tests and procedures as prescribed by the Bureau of Indian Standards, Association of Official Analytical Chemists International, American Oil Chemist's Society, and Standard methods given in the literature.  
                                                <br/> &nbsp;&nbsp;&nbsp;    Crude protein  
                                                <br/> &nbsp;&nbsp;&nbsp;•	Ether extract 
                                                <br/> &nbsp;&nbsp;&nbsp;•	Crude fiber  
                                                <br/> &nbsp;&nbsp;&nbsp;•	Ash 
                                                <br/> &nbsp;&nbsp;&nbsp;•	Neutral Detergent fiber  
                                                <br/> &nbsp;&nbsp;&nbsp;•	Minerals, phosphorus,  
                                                <br/> &nbsp;&nbsp;&nbsp;• Amino acid profile etc.  </p>
                                        </li>
                                        <li> <p>Standardization of Analytical Procedures for Estimation of Toxic Factors Standardization of Analytical procedures for estimation of the following toxic factors of Jatropha , Neem , Karanj cake samples was conducted following standard literature methods:   
                                                <br/> &nbsp;&nbsp;&nbsp;•	Jatropha-  Trypsin Inhibitor Activity, Saponin, Phorbol ester, Phytate, Tannin content                   
                                                <br/> &nbsp;&nbsp;&nbsp;•	Neem –Azadirachtin,  
                                                <br/> &nbsp;&nbsp;&nbsp;•	Karanj-Karanjin, Tannin (% of protein), Trypsin Inhibitor Activity Laboratory Scale Detoxification Experiments: Detoxification experiments were conducted  with  Jatropha, Neem, Karanja cake samples following the methods mentioned in project proposal e.g., treatment with Sodium chloride soln, treatment with Lime [Ca (OH)2] soln, water washing (overnight soaking  followed by decantation, Urea (fertilizer grade) ammoniation, alcohol treatment etc. Detoxified cake samples were analysed for chemical parameters & toxic components.
                                                <p> Laboratory scale detoxification experiments: were conducted  with  Jatropha, Neem, Karanj cake samples following the methods mentioned in project proposal e.g., treatment with sodium chloride solution, treatment with lime [Ca (OH)2] solution, water washing (overnight soaking  followed by decantation, urea (fertilizer grade) ammoniation, alcohol treatment etc. Detoxified cake samples were analysed for chemical parameters and toxic components. Alcohol extraction method was found to be the best effective method which removed 85-90% of total toxins. Recovery of anti-nutrients like karanjin from alcohol washings has been optimised</p>
                                                <p> Purchase of a solvent extraction plant (Capacity: 500 Liter) for large scale detoxification of cakes was placed completed from M/S.SSP Pvt. Limited Faridabad  and the plant was installed soon at Baddi unit of Ayurvet Ltd. <br/>The project was completed successfully and report duly submitted. </p>
                                         </p></li>
                                         <li> 
                                            <b> Benefits of the project: </b> <p>The project was proposed to be executed in mission mode, and will provide the base for:   </p>
                                            <ul>
                                                <li><p>Large-scale use of hitherto underutilized key non-conventional oil cakes after detoxification can substantially improve the protein availability and consequently cost effective livestock production. </p> </li>
                                                <li><p>Expansion of the existing feed base by value addition to nontraditional oil cakes for livestock. </p> </li>
                                                <li><p>An estimated benefit of Rs. 225 Crore annually to the feed industry. </p> </li>
                                                <li><p>Creating job opportunities for women and weaker sections of society.  </p> </li>
                                                <li><p>Other beneficiaries of the project will be farmers and livestock keepers, research community in livestock nutrition and feed technology, feed compounding industry and oil industries producing non-traditional oils and cakes.  </p>  </li>
                                            </ul>
                                         </li>
                                         
                                         
                                    </ul>
                                </li>
                                <li> 
                                    <b> PRODUCTION OF BIO-DIESEL FROM NON-TRADITIONAL OIL /OTHER   LOWCOST OILS BY   COST EFFECTIVE AND ENVIRONMENT FRIENDLY METHOD(IN HOUSE RESEARCH) </b>
                                    <ul>
                                        <li><p>Vegetable oils are main starting materials for producing biodiesel by interesterification reaction using base catalyst (sodium methoxide, sodium hydroxide etc.) and methanol. However, in   India, edible oils should better be preserved for edible purposes and cheaper starting materials should be exploited for production of biodiesel  to reduce production cost.</p> </li>
                                        <li><p>Such sources of cheaper starting material are low cost nontraditional oils having high free fatty acids, acid oil, (hydrolyzed product of soap-stock generated during vegetable oil refining process), and oil recovered from spent bleaching earth. There is great potential to create a value added bio-diesel product from such inexpensive material. Objective of the project was to develop technical know-how for production of bio-diesel from oil.</p> </li>
                                        <li><p> Work done in Laboratory Scale included detailed analysis, preparation of alcohol ester from low cost nontraditional oils by acid catalyzed alcoholysis, optimisation of reaction conditions and oil – Alcohol /acid /(Sulfuric) ratios. Procedure for separation of resultant fatty acid methyl ester (biodiesel) and glycerin, alcohol removal, and washing of the product was standardized. The methods were industrially feasible, efficient and cost effective to achieve a rapid, high efficiency synthesis of biodiesel from high FFA oils.</p></li>
                                        <li><p>Adoption of the resultant technology would provide excellent value addition for nontraditional oils, and an increased supply of economically affordable biodiesel to consumers.  </p> </li>
                                    </ul>
                                </li>
